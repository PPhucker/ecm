<?php

namespace App\Http\Requests\Contractor\Transport\Driver;

use App\Http\Requests\CoreFormRequest;

/**
 * Валидация обновления водителя контрагента.
 */
class UpdateDriverRequest extends CoreFormRequest
{
    /**
     * Get the validation rules that apply to the request.
     *
     * @return array
     */
    public function rules(): array
    {
        $prefix = 'drivers.*.';

        return [
            $prefix . 'id' => [
                'required',
                'numeric',
            ],
            $prefix . 'contractor_id' => [
                'required',
                'numeric',
            ],
            $prefix . 'name' => [
                'required',
                'string',
                'max:60',
            ],
        ];
    }
}
